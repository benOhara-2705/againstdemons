    const backButton = document.getElementById('back_button');
    let backButtonUrl = 'disc.html'; // the default for Radiohead
    let backButtonText = ' BACK TO DISCOGRAPHY';

    if (releaseID[0] === 'A') {
        backButtonUrl = 'atomsforpeace.html';
        backButtonText = ' BACK TO ATOMS FOR PEACE';
    } else if (releaseID[0] === 'S') {
        backButtonUrl = 'thesmile.html';
        backButtonText = ' BACK TO THE SMILE';
    } else if (releaseID[0] === 'T') {
        backButtonUrl = 'thom.html';
        backButtonText = ' BACK TO THOM YORKE';
    } else if (releaseID[0] === 'J') {
        backButtonUrl = 'jonny.html';
        backButtonText = ' BACK TO JONNY GREENWOOD';
    } else if (releaseID[0] === 'E') {
        backButtonUrl = 'ed.html';
        backButtonText = " BACK TO ED O'BRIEN";
    } else if (releaseID[0] === 'P') {
        backButtonUrl = 'philip.html';
        backButtonText = ' BACK TO PHILIP SELWAY';
    }

    backButton.onclick = () => document.location.href = backButtonUrl; // update the back button so that the href matches with the release artist
    backButton.innerHTML = backButtonText; // update the back button text as well
    
    // Next, the content of all the elements on the Release page will be altered to match the selected release
    document.title = (RData.title + ' | Against Demons')
    document.getElementById('release_box_cover_art').src = RData.imageID;
    document.getElementById('release_box_cover_art').alt = RData.title;

    document.getElementById('release_title').textContent = RData.title.toUpperCase();
    document.getElementById('release_artist').textContent = RData.artist.toUpperCase();
    document.getElementById('release_type_and_year').textContent = (RData.releaseType.toUpperCase() + ' / ' + RData.releaseYear);

    document.getElementById('previous_release_a_tag').href = ('release.html?id=' + RData.previousReleaseID);
    document.getElementById('previous_release_text').textContent = RData.previousReleaseTitle.toUpperCase();
    document.getElementById('previous_release_text').href = `release.html?id=${RData.previousReleaseID}`;
    document.getElementById('previous_release_button').onclick = () => window.location.href = `release.html?id=${RData.previousReleaseID}`;

    if (RData.artist == 'Radiohead') {
        document.getElementById('previous_release_icon').src = ('images/' + RData.previousReleaseID + '.png');
        document.getElementById('current_release_icon').src = ('images/' + releaseID + '.png');
        document.getElementById('next_release_icon').src = ('images/' + RData.nextReleaseID + '.png');
    } else {
        document.getElementById('previous_release_icon').src = ('images/Other' + RData.previousReleaseID.slice(1) + '.png');
        document.getElementById('current_release_icon').src = ('images/Other' + releaseID.slice(1) + '.png');
        document.getElementById('next_release_icon').src = ('images/Other' + RData.nextReleaseID.slice(1) + '.png');
    }

    document.getElementById('next_release_a_tag').href = ('release.html?id=' + RData.nextReleaseID);
    document.getElementById('next_release_text').href = `release.html?id=${RData.nextReleaseID}`;
    document.getElementById('next_release_text').textContent = RData.nextReleaseTitle.toUpperCase();
    document.getElementById('next_release_button').onclick = () => window.location.href = `release.html?id=${RData.nextReleaseID}`;

    // Then the colours of each element will be changed to reflect the section it was selected from
    document.getElementById('release_title').classList.add(RData.releaseSectionText);
    document.getElementById('release_artist').classList.add(RData.releaseSectionText);
    document.getElementById('release_type_and_year').classList.add(RData.releaseSectionText);
    document.getElementById('previous_release_button').classList.add(RData.releaseSection);
    document.getElementById('next_release_button').classList.add(RData.releaseSection);

    const trackListingContainer = document.getElementById('release_track_list_container'); // reference for the track listing container
    trackListingContainer.innerHTML = ''; // clear all of the content inside the track listing container

    if (RData.tracks && RData.tracks.length > 0) {
        let tracklistHtml = '<h1 id="track_listing_header">TRACK LISTING</h1>';
        if (RData.trackListCaption) {
            tracklistHtml += `<h3 id="track_listing_caption">${RData.trackListCaption}</h3>`;
        }
        tracklistHtml += `<div class="release_track_list">`;

        RData.tracks.forEach(track => { // for every track in the defined 'tracks' array of the object, create a div for each track in the releases' track listing
            tracklistHtml += `<div class="release_track ${RData.releaseSection}">
            <div class="release_track_details">
            <h1 class="release_track_title">${track.title}</h1>
            <div class="release_track_buttons">`;
            if (track.trackAudio) {
                tracklistHtml += ` <input type="image" src="images/AudioPlay.png" class="track_audio_button" data-audio-url="${track.trackAudio}">`;
            }
            if (track.trackVideo && track.videoAvailable) {
                tracklistHtml += ` <input type="image" src="images/VideoPlay.png" class="track_video_button" data-video-url="${track.trackVideo}">`;
            }
            tracklistHtml += `</div>
            </div>
            </div>`;
        });

        tracklistHtml += `</div>`;
        trackListingContainer.innerHTML = tracklistHtml;
    } else { // if the track list data for a certain release cannot be found
        document.getElementById('track_break_line').style.display = 'none';
        trackListingContainer.innerHTML = '<p>No tracklist available.</p>';
    }

    let currentlyActiveButton = null;

    const audioIconNormal = 'images/AudioPlay.png';
    const audioIconSelected = 'images/AudioPlaySelected.png';
    const videoIconNormal = 'images/VideoPlay.png';
    const videoIconSelected = 'images/VideoPlaySelected.png';

    if (trackListingContainer) {
        trackListingContainer.addEventListener('click', function(event){ // this will run everytime something is clicked in the track listing container
            const target = event.target; // the item that is clicked
            const isAudioButton = target.classList.contains('track_audio_button');
            const isVideoButton = target.classList.contains('track_video_button');

            if(!isAudioButton && !isVideoButton) { // if the item clicked isn't an audio or video button, do nothing
                return;
            }

            const releaseTrackDiv = target.closest('.release_track'); // reference to the track div itself
            if (!releaseTrackDiv) return;

            // get the URL for the audio/video from the button we selected
            const URLToEmbed = target.dataset.audioUrl || target.dataset.videoUrl;

            // we will check if any divs showing embeded videos are currently open
            const currentEmbed = document.querySelector('.track_video_div');
            let openVideoDiv = false;

            if (currentEmbed) { // if a video div is currently open, this will tell us if it belongs to the button we just selected
                if (currentEmbed.closest('.release_track') === releaseTrackDiv && currentEmbed.dataset.url === URLToEmbed) {
                    openVideoDiv = true;
                }
                currentEmbed.remove(); // either way, close the video div
            }

            if (currentlyActiveButton) { // reset the apperance of any button that may have been selected
                if (currentlyActiveButton.classList.contains('track_audio_button')) {
                    currentlyActiveButton.src = audioIconNormal;
                } else if (currentlyActiveButton.classList.contains('track_video_button')) {
                    currentlyActiveButton.src = videoIconNormal;
                }
                currentlyActiveButton = null;
            }

            if (openVideoDiv) { // if it does belong to the button we clicked, erase the contents of the div
                return;
            }

            if (isAudioButton) { // if the button selected is an audio button, change the apperance of the button
                target.src = audioIconSelected;
            } else if (isVideoButton) { // same for the video button
                target.src = videoIconSelected;
            }
            currentlyActiveButton = target; // remember the button that was just selected

            const embedURL = embedURLConvert(URLToEmbed); // if it doesn't, create the new URL and add it
            if (!embedURL) {
                console.log('ERROR: We could not create the URL for:' + URLToEmbed);
                return;
            }

            // create this HTML snippet to insert
            const embedHTML = `
                <div class="track_video_div ${RData.releaseSection}" data-url="${URLToEmbed}">
                    <iframe 
                        src="${embedURL}" 
                        width="100%" 
                        height="250" 
                        style= margin-top:5px;"
                        frameborder="0" 
                        allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" 
                        allowfullscreen>
                    </iframe>
                </div>
            `;

            releaseTrackDiv.insertAdjacentHTML('beforeend', embedHTML); // ...and insert it
        });
    }

    if(RData.previousReleaseID == "NA") { // on the earliest release, make the following changes:
        document.getElementById('previous_release_icon').style.display = 'none';
        document.getElementById('previous_release_text').style.display = 'none';
        document.getElementById('previous_release_button').style.display = 'none';
        document.getElementById('next_release_button').style.width = '50%';
        document.getElementById('release_navigation_buttons').style.justifyContent = 'flex-end';
        document.getElementById('release_navigation_button_links').style.justifyContent = 'flex-end';
    }

    if(RData.nextReleaseID == "NA") { // on the most recent release, make the following changes:
        document.getElementById('next_release_icon').style.display = 'none';
        document.getElementById('next_release_text').style.display = 'none';
        document.getElementById('next_release_button').style.display = 'none';
        document.getElementById('previous_release_button').style.width = '50%';
        document.getElementById('release_navigation_buttons').style.justifyContent = 'flex-start';
    }

